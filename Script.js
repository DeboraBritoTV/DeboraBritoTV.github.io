

function abreAlbum(){
  var abrir=document.querySelector(".capa");
  var positionfixed=document.querySelector("body");
  abrir.style.left='-100vw';
  abrir.style.transition='.5s';
  positionfixed.style.position='relative';
}

function voltarAlbum(){
  var voltar=document.querySelector(".capa");
  voltar.style.left='0';
  voltar.style.transition='.5s';
}

